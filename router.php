  function LGSLBuildRoute(&$query)
  {
    $segments   = array();
    $segments[] = $query['s'];
    unset($query['s']);

    return $segments;
  }

//------------------------------------------------------------------------------------------------------------+

  function LGSLParseRoute($segments)
  {
    $vars      = array();
    $vars['s'] = $segments[0];

    return $vars;
  }

//------------------------------------------------------------------------------------------------------------+
